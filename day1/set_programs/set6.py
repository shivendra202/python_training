# initialize my_set
# Output: set of unique elements
my_set = set("HelloWorld")
print(my_set)

# pop an element
# Output: random element
print(my_set.pop())

# pop another element
# Output: random element
my_set.pop()  # Randomly deletes an element
print(my_set)

# clear my_set
#Output: set()
my_set.clear() # The set is made empty
print(my_set)  # The set() exist    #That is set() is not deleted